/**
 * Tests for PR duplicate prevention and auto-close regression fixes.
 *
 * These tests cover the root causes of the "PRs created over and over" bug:
 * 1. Arrow format mismatch: PR bodies use both `->` (ASCII) and `→` (Unicode)
 * 2. Partial extraction leading to false "satisfied" closures
 * 3. Overly broad PR filters catching non-buddy-bot PRs
 * 4. False positives on "main" in PR body configuration text
 */

